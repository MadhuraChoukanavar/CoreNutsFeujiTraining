package com.cnstf.fourthweekstrings27_11_23;
//2. Write a program to wrap values into wrapper class objects (for all classes)
public class WrapValuesIntoWrapper {
	public static void main(String[] args) {
		Integer integer=new Integer(10);
		System.out.println(integer);
		Long lon=new Long(101);
		System.out.println(lon);
		Byte by=new Byte("1");
		System.out.println(by);
		Double dlb=new Double(0.111);
		System.out.println(dlb);
		Float flt=new Float(1.1f);
		System.out.println(flt);
		Character ch=new Character('c');
		System.out.println(ch);
		Boolean b=new Boolean(false);
		System.out.println(b);
		
		
	}

}
